export const MarkdownHeaderCodeBlock = ({
	id,
	code,
	language,
	title,
}: Props) => {
	const [copied, setCopied] = useState<boolean>(false);

	const handleCopy = async () => {
		try {
			setCopied(true);
			await navigator.clipboard.writeText(code);
			setTimeout(() => {
				setCopied(false);
			}, 1000);
		} catch (error) {
			console.error("Failed to copy code:", error);
		}
	};

	return (
		<div
			className="bg-muted border-border text-muted-foreground flex items-center gap-2 rounded-t-md border px-3 py-2"
			id={id}
		>
			<div className="flex items-center gap-2">
				{getFileIcons(title, language)}
				<span className="text-sm font-medium">{title}</span>
			</div>

			<Button
				type="button"
				variant="outline"
				size="icon"
				className="ml-auto size-8"
				onClick={handleCopy}
				disabled={copied}
			>
				{copied ? (
					<CheckIcon className="size-4" />
				) : (
					<CopyIcon className="size-4" />
				)}
			</Button>
		</div>
	);
};
